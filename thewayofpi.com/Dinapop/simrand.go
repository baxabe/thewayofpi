package main

import (
	"fmt"
	"time"
	"math/rand"
)

var dist [9][9]byte

func main() {
    for i := 0; i < 50000; i++ {
    	printWorld()
    	modRandDist()
    }
}

func modRandDist() {
	for i := 0; i < 9; i++ {
		for j := 0; j < 9; j++ {
			delta := rand.Intn(4)
			if rand.Intn(2) == 0 {
				delta = delta * -1
			}
			dist[i][j] = byte((int(dist[i][j]) + delta) % 256)
		}
	}
}

